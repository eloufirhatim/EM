package interfaces;

public interface Law {

	/*
	 * Prend en paramètre un double x
	 * Retourne la probabilité de la valeur x
	 */
	double proba(double x);
 
	/*
	 * -> Améliorer les paramètres du Law
	 * Prend en paramètres : 
	 * 		- N : le nombre d'échantillons
	 * 		- x : les echantillons
	 * 		- tk : la probabilité de chaque échantillon
	 */
	void improveParameters(int N, double[] x, double[] tk);
}
